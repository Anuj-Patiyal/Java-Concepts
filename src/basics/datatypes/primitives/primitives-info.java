package basics.datatypes.primitives;
/*
 * 
 * In Java language, primitive data types are the building blocks of data manipulation. 
     These are the most basic data types available in Java language. 
 
 * Java is a statically-typed programming language. 
 *      It means, all variables must be declared before its use. 
 *      That is why we need to declare variable's type and name.

 *  Data Type      Default Value         size            Range
 
    boolean          false               1 bit           true/false
 
    char           '\u0000'                              
        Stores a single character/letter or ASCII values
 
    byte              0                 1 byte           -128 to 127
    
    short             0                 2 byte           -32,768 to 32,767
    
    int               0                 4 byte           -2,147,483,648 to 2,147,483,647
    
    long              0L                8 byte           -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
 
 
    float             0.0f              4 byte
        Stores fractional numbers. Sufficient for storing 6 to 7 decimal digits
 
    double            0.0d              8 byte
        Stores fractional numbers. Sufficient for storing 15 to 16 decimal digits
*/